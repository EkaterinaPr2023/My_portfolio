//Переключение языка 

function translateModalContent(lang) {
    const modals = document.querySelectorAll('.folio-modal');
    modals.forEach(modal => {
        const elements = modal.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang]?.[key]) {
                el.textContent = translations[lang][key];
            }
        });
    });
}

function changeLanguage(lang) {
    // Обновляем основной контент
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });
    
    // Обновляем модальные окна
    setTimeout(() => {
        document.querySelectorAll('.folio-modal [data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang] && translations[lang][key]) {
                el.innerHTML = translations[lang][key];
            }
        });
    }, 50);
    
    window.currentLanguage = lang;
    
    // Обновляем активный флаг
    document.querySelectorAll('.language-switcher__flags a').forEach(flag => {
        flag.classList.toggle('active', flag.getAttribute('data-lang') === lang);
    });
}

// Инициализация
document.addEventListener('DOMContentLoaded', () => {
    // Обработчики для флагов
    document.querySelectorAll('.language-switcher__flags a').forEach(flag => {
        flag.addEventListener('click', (e) => {
            e.preventDefault();
            changeLanguage(flag.getAttribute('data-lang'));
        });
    });
    
    // Обработчик для модальных окон (ваш оригинальный код)
    document.querySelectorAll('.folio-list__item-link').forEach(link => {
        link.addEventListener('click', () => {
            setTimeout(() => {
                if (window.currentLanguage) {
                    changeLanguage(window.currentLanguage);
                }
            }, 100);
        });
    });
    
    // Русский язык по умолчанию
    changeLanguage('ru');
});